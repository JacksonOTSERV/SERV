function br2(creature, variant)
	local dir = creature:getDirection()
	if dir == 1 then
		combat1:execute(creature, variant)
		combat2:execute(creature, variant)
		combat3:execute(creature, variant)
		combat4:execute(creature, variant)
	elseif dir == 2 then
		combat13:execute(creature, variant)
		combat14:execute(creature, variant)
		combat15:execute(creature, variant)
		combat16:execute(creature, variant)
	elseif dir == 3 then
		combat5:execute(creature, variant)
		combat6:execute(creature, variant)
		combat7:execute(creature, variant)
		combat8:execute(creature, variant)
	elseif dir == 0 then
		combat9:execute(creature, variant)
		combat10:execute(creature, variant)
		combat11:execute(creature, variant)
		combat12:execute(creature, variant)
	end
	creature:say("Haa!", TALKTYPE_ORANGE_1)
end

function onCastSpell(creature, variant)
	br2(creature, variant)
    return true
